
package ejercicio1;

public class ejercicio_1 {
    public static void main(String[] args){
        //Tipos de datos enteros
        byte n1 = 10;
        int n2 = 100;
        short n3 = 1000;
        long n4  = 10000;
        System.out.println("Byte --> "+n1);
        System.out.println("Int --> "+n2);
        System.out.println("Short --> "+n3);
        System.out.println("Long --> "+n4);
        //Tipos de datos Decimal 
        float num1 = 1.5F;
        double num2 = 2.3;
        System.out.println("Float --> "+num1);
        System.out.println("Double --> "+num2);
        //Tipos de datos de Texto
        String txt1 = "El pepe";
        char txt2 = 'a';
        System.out.println("String --> "+txt1);
        System.out.println("Char --> "+txt2);
    }
}
